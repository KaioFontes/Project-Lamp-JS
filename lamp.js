//Settings of the lamp

//function to turn on the lamp
turnOn = () =>{
    document.getElementById('image').src = 'img/ligada.jpg';
}

//Function to turn off the lamp
turnOff = () =>{
    document.getElementById('image').src = 'img/desligada.jpg';
}

//Function to Break the lamp
lampBreak = () =>{
    document.getElementById('image').src = 'img/quebrada.jpg';
}